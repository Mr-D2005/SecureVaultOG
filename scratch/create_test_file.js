const fs = require('fs');

// Create a valid but empty-ish PNG buffer
// 8-byte signature
const signature = Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A]);
// IHDR chunk
const ihdr = Buffer.from([
    0x00, 0x00, 0x00, 0x0D, // length
    0x49, 0x48, 0x44, 0x52, // type
    0x00, 0x00, 0x00, 0x01, // width 1
    0x00, 0x00, 0x00, 0x01, // height 1
    0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, etc.
    0x90, 0x77, 0x53, 0xDE  // CRC
]);
// IDAT chunk (minimal)
const idat = Buffer.from([
    0x00, 0x00, 0x00, 0x0A,
    0x49, 0x44, 0x41, 0x54,
    0x08, 0xD7, 0x63, 0x60, 0x00, 0x00, 0x00, 0x02, 0x00, 0x01,
    0xE2, 0x21, 0xBC, 0x33
]);
// IEND chunk
const iend = Buffer.from([0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82]);

const normalPng = Buffer.concat([signature, ihdr, idat, iend]);
fs.writeFileSync('c:\\Users\\hp\\Desktop\\SecureVaultOG\\normal_test.png', normalPng);
console.log('Created normal_test.png');
